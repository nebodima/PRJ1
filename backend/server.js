app.post('/api/tasks/:id/comments', async (req, res) => {
  try {
    const { text, userId } = req.body;
    if (!text || !userId) {
      return res.status(400).json({ error: '–û—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ –ø–æ–ª—è' });
    }

    const user = await getUserById(userId);
    if (!user) {
      return res.status(404).json({ error: '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω' });
    }

    const comment = {
      id: Date.now().toString(),
      text,
      userId,
      userName: user.name,
      createdAt: new Date().toISOString()
    };

    const result = await addComment(req.params.id, comment);
    if (!result) {
      return res.status(404).json({ error: '–ó–∞–¥–∞—á–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞' });
    }

    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º push-—É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
    try {
      const task = await getTaskById(req.params.id);
      await sendNotificationToAll({
        title: 'üí¨ –ù–æ–≤—ã–π –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π',
        body: `${user.name}: ${text.substring(0, 50)}${text.length > 50 ? '...' : ''}`,
        icon: '/icon-192x192.png',
        badge: '/icon-72x72.png',
        tag: `comment-${req.params.id}`,
        data: { taskId: req.params.id, commentId: comment.id, type: 'new_comment' }
      });
    } catch (pushError) {
      console.error('Push notification failed:', pushError);
    }

    res.status(201).json(result);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

