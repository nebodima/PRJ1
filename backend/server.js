import express from 'express';
import cors from 'cors';
import path from 'path';
import { fileURLToPath } from 'url';
import multer from 'multer';
import fs from 'fs';
import { getUsers, getTasks, createTask, updateTask, deleteTask, authenticateUser, getUserById, addAttachment, deleteAttachment, getTaskById, addComment } from './database.js';
import { addSubscription, removeSubscription, sendNotificationToAll } from './push-service.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const app = express();
const PORT = process.env.PORT || 3000;

// –°–æ–∑–¥–∞–µ–º –ø–∞–ø–∫—É –¥–ª—è uploads –µ—Å–ª–∏ –µ—ë –Ω–µ—Ç
// –ï—Å–ª–∏ /data —Å—É—â–µ—Å—Ç–≤—É–µ—Ç (Railway Volume) - –∏—Å–ø–æ–ª—å–∑—É–µ–º –µ–≥–æ, –∏–Ω–∞—á–µ –ª–æ–∫–∞–ª—å–Ω—É—é –ø–∞–ø–∫—É
const uploadsDir = fs.existsSync('/data')
  ? '/data/uploads'
  : path.join(__dirname, 'uploads');

if (!fs.existsSync(uploadsDir)) {
  fs.mkdirSync(uploadsDir, { recursive: true });
}

console.log(`‚úì Uploads directory: ${uploadsDir}`);

// –ù–∞—Å—Ç—Ä–æ–π–∫–∞ Multer
const storage = multer.diskStorage({
  destination: (req, file, cb) => {
    cb(null, uploadsDir);
  },
  filename: (req, file, cb) => {
    const uniqueSuffix = Date.now() + '-' + Math.round(Math.random() * 1E9);
    cb(null, uniqueSuffix + '-' + file.originalname);
  }
});

const upload = multer({
  storage,
  limits: { fileSize: 10 * 1024 * 1024 } // 10MB
});

app.use(cors());
app.use(express.json());

// –†–∞–∑–¥–∞—á–∞ —Å—Ç–∞—Ç–∏–∫–∏ frontend –≤ production
const frontendPath = path.join(__dirname, '../frontend/dist');
app.use(express.static(frontendPath));

app.post('/api/login', async (req, res) => {
  try {
    const { login, password } = req.body;
    const user = await authenticateUser(login, password);

    if (user) {
      res.json({ success: true, user });
    } else {
      res.status(401).json({ success: false, message: '–ù–µ–≤–µ—Ä–Ω—ã–π –ª–æ–≥–∏–Ω –∏–ª–∏ –ø–∞—Ä–æ–ª—å' });
    }
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

app.get('/api/users', async (req, res) => {
  try {
    const users = await getUsers();
    res.json(users);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

app.get('/api/tasks', async (req, res) => {
  try {
    const tasks = await getTasks();
    res.json(tasks);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

app.post('/api/tasks', async (req, res) => {
  try {
    const taskId = await createTask(req.body);
    const tasks = await getTasks();
    const newTask = tasks.find(t => t.id === taskId);
    
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º push-—É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
    try {
      await sendNotificationToAll({
        title: 'üìã –ù–æ–≤–∞—è –∑–∞–¥–∞—á–∞',
        body: `${newTask.title}`,
        icon: '/icon-192x192.png',
        badge: '/icon-72x72.png',
        tag: `task-${taskId}`,
        data: { taskId, type: 'new_task' }
      });
    } catch (pushError) {
      console.error('Push notification failed:', pushError);
    }
    
    res.status(201).json(newTask);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

app.put('/api/tasks/:id', async (req, res) => {
  try {
    await updateTask(req.params.id, req.body);
    const tasks = await getTasks();
    const updatedTask = tasks.find(t => t.id === parseInt(req.params.id));
    res.json(updatedTask);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

app.delete('/api/tasks/:id', async (req, res) => {
  try {
    // –ü–æ–ª—É—á–∞–µ–º –∑–∞–¥–∞—á—É –ø–µ—Ä–µ–¥ —É–¥–∞–ª–µ–Ω–∏–µ–º –¥–ª—è –¥–æ—Å—Ç—É–ø–∞ –∫ —Ñ–∞–π–ª–∞–º
    const task = await getTaskById(req.params.id);

    if (task && task.attachments && task.attachments.length > 0) {
      // –£–¥–∞–ª—è–µ–º –≤—Å–µ —Ñ–∏–∑–∏—á–µ—Å–∫–∏–µ —Ñ–∞–π–ª—ã –∑–∞–¥–∞—á–∏
      task.attachments.forEach(attachment => {
        const filePath = path.join(uploadsDir, attachment.filename);
        if (fs.existsSync(filePath)) {
          try {
            fs.unlinkSync(filePath);
            console.log(`‚úì Deleted file: ${attachment.filename}`);
          } catch (err) {
            console.error(`‚úó Error deleting file ${attachment.filename}:`, err);
          }
        }
      });
    }

    // –£–¥–∞–ª—è–µ–º –∑–∞–¥–∞—á—É –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö
    await deleteTask(req.params.id);
    res.status(204).send();
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

// –ó–∞–≥—Ä—É–∑–∫–∞ —Ñ–∞–π–ª–∞ –∫ –∑–∞–¥–∞—á–µ
app.post('/api/tasks/:id/attachments', upload.single('file'), async (req, res) => {
  try {
    if (!req.file) {
      return res.status(400).json({ error: '–§–∞–π–ª –Ω–µ –∑–∞–≥—Ä—É–∂–µ–Ω' });
    }

    const attachment = {
      id: Date.now().toString(),
      filename: req.file.filename,
      originalName: req.file.originalname,
      size: req.file.size,
      uploadDate: new Date().toISOString()
    };

    const result = await addAttachment(req.params.id, attachment);
    if (!result) {
      // –£–¥–∞–ª—è–µ–º —Ñ–∞–π–ª –µ—Å–ª–∏ –∑–∞–¥–∞—á–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞
      fs.unlinkSync(req.file.path);
      return res.status(404).json({ error: '–ó–∞–¥–∞—á–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞' });
    }

    res.status(201).json(attachment);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

// –£–¥–∞–ª–µ–Ω–∏–µ —Ñ–∞–π–ª–∞ –∏–∑ –∑–∞–¥–∞—á–∏
app.delete('/api/tasks/:taskId/attachments/:attachmentId', async (req, res) => {
  try {
    const task = await getTaskById(req.params.taskId);
    if (!task) {
      return res.status(404).json({ error: '–ó–∞–¥–∞—á–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞' });
    }

    const attachment = task.attachments.find(a => a.id === req.params.attachmentId);
    if (!attachment) {
      return res.status(404).json({ error: '–§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω' });
    }

    // –£–¥–∞–ª—è–µ–º —Ñ–∏–∑–∏—á–µ—Å–∫–∏–π —Ñ–∞–π–ª
    const filePath = path.join(uploadsDir, attachment.filename);
    if (fs.existsSync(filePath)) {
      fs.unlinkSync(filePath);
    }

    await deleteAttachment(req.params.taskId, req.params.attachmentId);
    res.status(204).send();
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

